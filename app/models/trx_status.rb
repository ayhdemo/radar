  initialized.nil? && %W(
  NEW

  IN_CONFIRMED

  AUTO_OUT_PROCESSING
  AUTO_OUT_PROCESSED
  AUTO_OUT_CONFIRMED
  AUTO_OUT_ERR_BAD_ADDRESS
  AUTO_OUT_ERR_GW_DISABLED
  AUTO_OUT_ERR_OUT_TO_GW
  AUTO_OUT_ERR_ASSET_UNKNOWN
  AUTO_OUT_ERR_ASSET_DISABLED
  AUTO_OUT_ERR_OUT_CHAIN_DISABLED
  AUTO_OUT_ERR_OUT_CHAIN_NOT_FOUND
  AUTO_OUT_ERR_OUT_CHAIN_NOT_RELIABLE
  AUTO_OUT_ERR_OUT_FROM_ACCOUNT_NOT_FOUND
  AUTO_OUT_ERR_OUT_FROM_ACCOUNT_DISABLED
  AUTO_OUT_ERR_INSUFFICIENT_BALANCE
  AUTO_OUT_ERR_AMOUNT_LARGE_THAN_IN
  AUTO_OUT_ERR_UNKNOWN


  MANUAL_OUT_PROCESSING
  MANUAL_OUT_PROCESSED
  MANUAL_OUT_CONFIRMED
  MANUAL_OUT_ERR_OUT_DISABLED
  MANUAL_OUT_ERR_OUT_FROM_ACCOUNT_NOT_FOUND
  MANUAL_OUT_ERR_OUT_FROM_ACCOUNT_DISABLED
  MANUAL_OUT_ERR_INSUFFICIENT_BALANCE
  MANUAL_OUT_ERR_AMOUNT_LARGE_THAN_IN
