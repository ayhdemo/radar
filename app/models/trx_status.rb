  IN_ERR_ETH_NOT_BIND
  IN_ERR_ERC20_NOT_BIND
  IN_ERR_BTC_NOT_BIND
  IN_ERR_OMNI_NOT_BIND
  IN_ERR_UNKNOWN
 ).each {|k|
    define k.to_sym, k.to_s.downcase
  }
end